from bisect import bisect

def insort(li:list,x:int,low:int=0,high:int=None):
    if high == None : 
        high = len(li) - 1;

    # find the index
    index =  bisect(li,x,low,high);
    li.insert(index,x);

    return li;

if __name__ == "__main__":
    for n in range(11):
        li = [1,2,2,3,4,6,8,9];

        print("> li : ",li);
        print("> li <-(",n,") insert");
        insort(li,n);
        print("> li : ",li);
        print("-------------------------------\n");

